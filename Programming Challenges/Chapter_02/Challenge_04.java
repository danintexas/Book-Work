/**     Star Pattern
 * Write a program that displays a star pattern with '*'
 */

public class Challenge_04 
{
    public static void main(String[] args)
    {
        System.out.println("   *");
        System.out.println("  ***");
        System.out.println(" *****");
        System.out.println("*******");
        System.out.println(" *****");
        System.out.println("  ***");
        System.out.println("   *");
        
        // Another way for fun
        System.out.println("\nAnother way for the hell of it\n");
        System.out.println("   *\n  ***\n *****\n*******\n" +
                " *****\n  ***\n   *");
        
    }
}
